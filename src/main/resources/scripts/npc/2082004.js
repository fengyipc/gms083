var status;

function start() {
        status = -1;
        action(1, 0, 0);
}

function action(mode, type, selection) {
        if (mode == -1) {
                cm.dispose();
        } else {
                if (mode == 0 && type > 0) {
                        cm.dispose();
                        return;
                }
                if (mode == 1)
                        status++;
                else
                        status--;
        
                if(status == 0) {
                        cm.sendOk("你好,我是#p2082004#,从不远的将来回来的时光旅行者.我是来阻止这个时代的人制造机器人的。那些机器人在我的时空里发狂，把所有的东西都烧成灰。我必须不惜一切代价阻止它！");
                        cm.dispose();
                }
        }
}
